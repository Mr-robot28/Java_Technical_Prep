package com.code.l1;

// Write a program to read the length and breadth of a rectangle and print its area and perimeter.
public class L1_Q5 {
	
	public static void main(String[] args) {
		
		int l = 10;
		int b = 20;
		
		int area = (l*b);
		int perimeter = 2 * (l+b);
		
		System.out.println("Area : "+ area);
		System.out.println("Perimeter : "+ perimeter);
	}

}
